export function TranslationProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('credlocity_lang') || 'en');

  useEffect(() => {
    localStorage.setItem('credlocity_lang', lang);
  }, [lang]);

  const t = (key) => translations[lang]?.[key] || translations.en[key] || key;
  const toggleLang = () => setLang(prev => prev === 'en' ? 'es' : 'en');

  return (
    <TranslationContext.Provider value={{ lang, setLang, t, toggleLang }}>
      {children}
    </TranslationContext.Provider>
  );
}

export function useTranslation() {
  const ctx = useContext(TranslationContext);
  if (!ctx) return { lang: 'en', setLang: () => {}, t: (k) => k, toggleLang: () => {} };
  return ctx;
}

export default TranslationContext;
